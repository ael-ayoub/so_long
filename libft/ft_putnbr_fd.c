#include "libft.h"

void	ft_putnbr_fd(int nb, int fd)
{
	int	nbr;

	nbr = nb;
	if (nbr < 0)
	{
		if (nbr == -2147483648)
		{
			ft_putchar_fd('-', fd);
			ft_putchar_fd('2', fd);
			ft_putnbr_fd(147483648, fd);
		}
		else
		{
			ft_putchar_fd('-', fd);
			nbr *= -1;
			ft_putnbr_fd(nbr, fd);
		}
	}
	else if (nbr > 9)
	{
		ft_putnbr_fd(nbr / 10, fd);
		ft_putnbr_fd(nbr % 10, fd);
	}
	else
		ft_putchar_fd(nbr + 48, fd);
}
